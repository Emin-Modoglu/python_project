import platform

# İşletim sistemini öğrenmek için -->  
print(platform.system())


# İşlemci bilgisi öğrenmek için -->
print(platform.processor())


# Python Versiyonu öğrenmek için -->
print(platform.python_version())


# Bilgisayarın kullanıcı adını öğrenmek için -->
print(platform.node())


#  Python compiler bilgisini öğrenmek için -->
print(platform.python_compiler())


# Python implementasyonu öğrenmek için -->
print(platform.python_implementation())